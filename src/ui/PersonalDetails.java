    private void pickDate() {
        // Add a date picker for picking a date. Not that great but works.
        JButton pickButton = new JButton("Pick a date");
        // Also give an option for manually entering a date
        JButton submitDate = new JButton("Submit date manually");
        final JFormattedTextField dateInput = new JFormattedTextField();
        JLabel labelDate = new JLabel(" or write date manually ", JLabel.CENTER);
        JLabel labelFormat = new JLabel(" DD / MM / YYYY ", JLabel.CENTER);
        try {
            // Set a pre defined format for setting the date
            dateInput.setFormatterFactory(new DefaultFormatterFactory(new MaskFormatter("HH-HH-HHHH")));
        } catch (ParseException ex) {
            Logger.getLogger(PersonalDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
        JPanel backgroundPanel = new JPanel();
        backgroundPanel.add(pickButton);
        backgroundPanel.add(labelDate);
        backgroundPanel.add(dateInput);
        backgroundPanel.add(labelFormat);
        backgroundPanel.add(submitDate);
        final JFrame backgroundFrame = new JFrame();
        backgroundFrame.getContentPane().add(backgroundPanel);
        backgroundFrame.pack();
        backgroundFrame.setVisible(true);
        submitDate.addActionListener((var ae) -> {
            if (!userProfile.validateDate(dateInput.getText())) {
                backgroundFrame.setVisible(false);
                // Use a validator for checking the date
                JOptionPane.showMessageDialog(this, "Please enter a valid date.");
                return;
            }
            userProfile.setDob(dateInput.getText());
            datePicker.setText(dateInput.getText());
            backgroundPanel.setVisible(false);
            backgroundFrame.setVisible(false);
        });
        pickButton.addActionListener((var ae) -> {
            datePicker.setText(new DatePicker(backgroundFrame).setPickedDate());
            backgroundFrame.setVisible(false);
            backgroundPanel.setVisible(false);
        });
    }
    
    private void chooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseFileActionPerformed
        // Add filter for selecting only images
        FileFilter filterImages = new FileNameExtensionFilter("images","jpeg", "png", "jpg");
        
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(filterImages);
        int open = fileChooser.showOpenDialog(this);

        if (open == javax.swing.JFileChooser.APPROVE_OPTION){

            // Get the path of the selected file
            File path = fileChooser.getSelectedFile();

            // Convert the path to string
            String file_name = path.toString();
            
            // Save it as a variable for calling it later while displaying user profile photo
            userProfile.setPhotoPath(file_name);

            // Set the button's name as the file path
            chooseFile.setText(file_name);
        }
    }//GEN-LAST:event_chooseFileActionPerformed

