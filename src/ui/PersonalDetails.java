    private void storeFields() {
        // Using the setters in the UserProfile class to store inputs in memory
        userProfile.setFirstName(firstNameInput.getText());
        userProfile.setLastName(lastNameInput.getText());
        userProfile.setEmailAddress(emailInput.getText());
        userProfile.setPersonAddress(addressInput.getText());
        userProfile.setPersonNumber(contactNumberInput.getText());
        userProfile.setAge(datePicker.getText());
    }
    
    private void validateFields() {
        // Validations for grabbing the correct inputs from the user
        if (!userProfile.validateName(firstNameInput.getText())) {
            JOptionPane.showMessageDialog(this, "Please enter a valid name.");
            return;
        }
        if(!userProfile.validateName(lastNameInput.getText())) {
            JOptionPane.showMessageDialog(this, "Please enter a valid surname.");
            return;
        }
        if (!userProfile.validatePhone(contactNumberInput.getText())) {
            JOptionPane.showMessageDialog(this, "Please enter a valid contact.");
            return;
        }
        if(!userProfile.validateEmail(emailInput.getText())) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Email-ID.");
            return;
        }
        if(!userProfile.validateDate(datePicker.getText())) {
            JOptionPane.showMessageDialog(this, "Please enter a valid date.");
            return;
        }
        if(userProfile.getPhotoPath() == null) {
            JOptionPane.showMessageDialog(this, "Please select a profile photo first.");
            return;
        }
        openNextPanel();
    }
    
